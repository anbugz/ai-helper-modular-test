"""
handlers/commands.py — базовые команды /start, /help.
/clear УБРАН по требованию брифа.
"""
from aiogram import Router
from aiogram.filters import Command
from aiogram.types import Message

router = Router()


@router.message(Command("start"))
async def cmd_start(message: Message):
    name = message.from_user.first_name or "Коллега"
    await message.answer(
        f"👋 <b>Привет, {name}!</b>\n"
        f"Я — AI-помощник <b>West Asia</b> для менеджеров по ВЭД.\n\n"

        "━━━━━━━━━━━━━━━━━━━━━\n"
        "📦 <b>1. РАСЧЁТ ТАМОЖЕННЫХ ПЛАТЕЖЕЙ</b>\n"
        "Напиши код ТН ВЭД + сумму + условия — посчитаю пошлину, НДС и сборы.\n\n"
        "<i>Примеры:</i>\n"
        "• <code>6104130000 15000ю фрахт 900дол страховка 200ю вес 500кг</code>\n"
        "• <code>8517120000 инвойс 50000ю вес 300кг фрахт 2000дол</code>\n"
        "• <code>6109100000 инвойс 300000р фрахт 15000р вес 100кг</code>\n\n"
        "Валюты: <b>ю</b> = юань, <b>дол</b> = доллар, <b>евр</b> = евро, <b>р</b> = рубль\n\n"

        "━━━━━━━━━━━━━━━━━━━━━\n"
        "🔍 <b>2. ПОДБОР КОДА ТН ВЭД</b>\n"
        "Опиши товар — помогу с кодом и задам уточняющие вопросы.\n\n"
        "<i>Примеры:</i>\n"
        "• <code>подбери код для хлопковых мужских футболок</code>\n"
        "• <code>какой код для алюминиевых профилей</code>\n"
        "• <code>код тн вэд на детские игрушки пластик</code>\n\n"

        "━━━━━━━━━━━━━━━━━━━━━\n"
        "👥 <b>3. АГЕНТЫ И ЭКСПЕДИТОРЫ</b>\n"
        "Найду контакты нужного экспедитора по виду транспорта.\n\n"
        "<i>Примеры:</i>\n"
        "• <code>покажи агентов авиа</code>\n"
        "• <code>наши экспедиторы авто</code>\n"
        "• <code>кто занимается жд доставкой</code>\n"
        "• <code>агенты море жд</code>\n\n"

        "━━━━━━━━━━━━━━━━━━━━━\n"
        "💱 <b>4. КУРСЫ ВАЛЮТ</b>\n"
        "Курс ЦБ РФ добавляется автоматически к каждому расчёту.\n\n"
        "<i>Примеры:</i>\n"
        "• <code>переведи 10000 юаней в рубли</code>\n"
        "• <code>сколько будет 5000 долларов в рублях</code>\n\n"

        "━━━━━━━━━━━━━━━━━━━━━\n"
        "🎤 <b>5. ГОЛОСОВЫЕ СООБЩЕНИЯ</b>\n"
        "Отправь голосовое — распознаю и обработаю как текстовый запрос.\n\n"

        "━━━━━━━━━━━━━━━━━━━━━\n"
        "🌐 <b>6. ВОПРОСЫ ПО ВЭД</b>\n"
        "Задай любой вопрос по таможне, логистике, документам.\n\n"
        "<i>Примеры:</i>\n"
        "• <code>какие документы нужны для импорта одежды из Китая</code>\n"
        "• <code>что такое CIF и как считается таможенная стоимость</code>\n"
        "• <code>сроки таможенного оформления</code>\n\n"

        "━━━━━━━━━━━━━━━━━━━━━\n"
        "💡 <b>Полезно знать:</b>\n"
        "• Если ответ неточный — <b>ответь на моё сообщение</b> словом «несогласен»\n"
        "• Точные ставки и коды всегда уточняй у декларанта\n"
        "• /help — краткая справка в любой момент",
        parse_mode="HTML"
    )


@router.message(Command("help"))
async def cmd_help(message: Message):
    await message.answer(
        "<b>📋 Краткая справка:</b>\n\n"
        "🔢 <b>Расчёт:</b> код ТН ВЭД + сумма + вес\n"
        "<code>6104130000 15000ю фрахт 900дол вес 500кг</code>\n\n"
        "🔍 <b>Подбор кода:</b> опиши товар\n"
        "<code>подбери код для хлопковых футболок</code>\n\n"
        "👥 <b>Агенты:</b> вид транспорта\n"
        "<code>покажи агентов авиа</code>\n\n"
        "🌐 <b>ВЭД-вопросы:</b> пиши свободно\n"
        "<code>какие документы нужны для импорта из Китая</code>\n\n"
        "🎤 <b>Голосовые:</b> просто отправь запись\n\n"
        "<i>Если ответ неточный — ответь «несогласен» на моё сообщение.</i>",
        parse_mode="HTML"
    )
