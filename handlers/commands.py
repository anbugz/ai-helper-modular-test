    await message.answer(
        "<b>West Asia AI Helper</b> — внутренний ассистент для менеджеров по ВЭД.\n\n"
        "<b>Что умею:</b>\n\n"
        "📦 <b>ТН ВЭД и таможня</b>\n"
        "• Отправь код → пошлина, НДС, сборы\n"
        "• Опиши товар → подберу код\n"
        "Пример: <code>6109100000</code> или <code>подбери код для футболок</code>\n\n"
        "🧮 <b>Расчёт таможенных платежей</b>\n"
        "Код + инвойс + фрахт — полный расчёт автоматически\n"
        "Пример: <code>6109100000 инвойс 15000 юаней фрахт 900 долларов</code>\n\n"
        "💱 <b>Курсы валют и конвертация</b>\n"
        "Пример: <code>курс юаня</code> или <code>переведи 50000 юаней в рубли</code>\n\n"
        "📋 <b>AmoCRM — сделки и контакты</b>\n"
        "• Найду сделку, покажу статус и поля\n"
        "• Найду реквизиты клиента\n"
        "• Добавлю примечание в карточку\n"
        "Пример: <code>найди сделку Игрушки</code> или <code>реквизиты клиента Иванов</code>\n\n"
        "✅ <b>Задачи и напоминания</b>\n"
        "• Создам задачу голосом или текстом\n"
        "• Понимаю: «через полчаса», «завтра в 10», «через два дня»\n"
        "Пример: <code>напомни позвонить клиенту через полчаса</code>\n\n"
        "🤖 <b>AI-ассистент по ВЭД</b>\n"
        "• Вопросы по логистике, таможне, инкотермс\n"
        "• Агенты, декларанты, партнёры из базы знаний\n"
        "Пример: <code>кто наш декларант</code> или <code>варианты доставки из Китая</code>\n\n"
        "☀️ <b>Утренняя рассылка</b> — каждый день в 9:15 МСК\n"
        "Просроченные и срочные задачи по твоим сделкам\n\n"
        "🎤 <b>Голосовые сообщения</b> — всё работает голосом\n\n"
        "Если ответ неправильный — ответь на моё сообщение словом «несогласен».",
        parse_mode="HTML"
    )


